    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed
        // TODO add your handling code here:
        Poupanca p = new Poupanca();

        p.setAgencia(jTextFieldAgencia.getText());
        p.setNumero(jTextFieldNumero.getText());
        p.setSaldo(Double.parseDouble(jTextFieldSaldo.getText()));
        p.setNomeCliente(c.getNome());
        p.setRendimento(Double.parseDouble(jTextFieldRendimento.getText()));
        
        DecimalFormat df = new DecimalFormat("#.00");
        
        JOptionPane.showMessageDialog(null, 
                "Nome: " + c.getNome() +
                "\nEndereço: " + c.getEndereco() +
                "\nRG: " + c.getRg() + 
                "\nTelefone: " + c.getTelefone() +
                "\nAgência: " + p.getAgencia() +
                "\nNúmero: " + p.getNumero() +
                "\nSaldo: " + df.format(p.getSaldo()) +
                "\nNome do cliente: " + p.getNomeCliente() +
                "\nRendimento: " + df.format(p.getRendimento()));
    }//GEN-LAST:event_jButtonOKActionPerformed

